import java.util.*;
public class DataTypeDemonstration{
    public static void main(String[] args) {
        int age = 21;
        float price = 99.99f;
        double pi = 3.14159;
        char grade = 'A';
        boolean isJavaFun = true;

        System.out.println("---- Data Type Demonstration ----");
        System.out.println("Integer (int): " + age);
        System.out.println("Float (float): " + price);
        System.out.println("Double (double): " + pi);
        System.out.println("Character (char): " + grade);
        System.out.println("Boolean (boolean): " + isJavaFun);
    }
}

